/// Base values for the Prism theme.
class PrismBase {
  /// Creates a new instance of [PrismBase].
  const PrismBase();

  /// XXS size.
  double get xxs => 2;

  /// XS size.
  double get xs => 4;

  /// S size.
  double get s => 8;

  /// M size.
  double get m => 12;

  /// L size.
  double get l => 16;

  /// XL size.
  double get xl => 24;

  /// XXL size.
  double get xxl => 32;

  /// XXXL size.
  double get xxxl => 64;
}

/// Base values for the Prism theme.
const base = PrismBase();
