import 'package:flutter/material.dart';
import 'package:saga/theme/base.dart';

/// Padding values for the Prism theme.
class PrismPadding {
  /// Creates a new instance of [PrismPadding].
  const PrismPadding();

  /// Top padding XXS.
  EdgeInsets get tXXS => EdgeInsets.only(top: base.xxs);

  /// Top padding XS.
  EdgeInsets get tXS => EdgeInsets.only(top: base.xs);

  /// Top padding S.
  EdgeInsets get tS => EdgeInsets.only(top: base.s);

  /// Top padding M.
  EdgeInsets get tM => EdgeInsets.only(top: base.m);

  /// Top padding L.
  EdgeInsets get tL => EdgeInsets.only(top: base.l);

  /// Top padding XL.
  EdgeInsets get tXL => EdgeInsets.only(top: base.xl);

  /// Top padding XXL.
  EdgeInsets get tXXL => EdgeInsets.only(top: base.xxl);

  /// Right padding XXS.
  EdgeInsets get rXXS => EdgeInsets.only(right: base.xxs);

  /// Right padding XS.
  EdgeInsets get rXS => EdgeInsets.only(right: base.xs);

  /// Right padding S.
  EdgeInsets get rS => EdgeInsets.only(right: base.s);

  /// Right padding M.
  EdgeInsets get rM => EdgeInsets.only(right: base.m);

  /// Right padding L.
  EdgeInsets get rL => EdgeInsets.only(right: base.l);

  /// Right padding XL.
  EdgeInsets get rXL => EdgeInsets.only(right: base.xl);

  /// Right padding XXL.
  EdgeInsets get rXXL => EdgeInsets.only(right: base.xxl);

  /// Bottom padding XXS.
  EdgeInsets get bXXS => EdgeInsets.only(bottom: base.xxs);

  /// Bottom padding XS.
  EdgeInsets get bXS => EdgeInsets.only(bottom: base.xs);

  /// Bottom padding S.
  EdgeInsets get bS => EdgeInsets.only(bottom: base.s);

  /// Bottom padding M.
  EdgeInsets get bM => EdgeInsets.only(bottom: base.m);

  /// Bottom padding L.
  EdgeInsets get bL => EdgeInsets.only(bottom: base.l);

  /// Bottom padding XL.
  EdgeInsets get bXL => EdgeInsets.only(bottom: base.xl);

  /// Bottom padding XXL.
  EdgeInsets get bXXL => EdgeInsets.only(bottom: base.xxl);

  /// Left padding XXS.
  EdgeInsets get lXXS => EdgeInsets.only(left: base.xxs);

  /// Left padding XS.
  EdgeInsets get lXS => EdgeInsets.only(left: base.xs);

  /// Left padding S.
  EdgeInsets get lS => EdgeInsets.only(left: base.s);

  /// Left padding M.
  EdgeInsets get lM => EdgeInsets.only(left: base.m);

  /// Left padding L.
  EdgeInsets get lL => EdgeInsets.only(left: base.l);

  /// Left padding XL.
  EdgeInsets get lXL => EdgeInsets.only(left: base.xl);

  /// Left padding XXL.
  EdgeInsets get lXXL => EdgeInsets.only(left: base.xxl);

  /// Horizontal padding XXS.
  EdgeInsets get xXXS => EdgeInsets.symmetric(horizontal: base.xxs);

  /// Horizontal padding XS.
  EdgeInsets get xXS => EdgeInsets.symmetric(horizontal: base.xs);

  /// Horizontal padding S.
  EdgeInsets get xS => EdgeInsets.symmetric(horizontal: base.s);

  /// Horizontal padding M.
  EdgeInsets get xM => EdgeInsets.symmetric(horizontal: base.m);

  /// Horizontal padding L.
  EdgeInsets get xL => EdgeInsets.symmetric(horizontal: base.l);

  /// Horizontal padding XL.
  EdgeInsets get xXL => EdgeInsets.symmetric(horizontal: base.xl);

  /// Horizontal padding XXL.
  EdgeInsets get xXXL => EdgeInsets.symmetric(horizontal: base.xxl);

  /// Vertical padding XXS.
  EdgeInsets get yXXS => EdgeInsets.symmetric(vertical: base.xxs);

  /// Vertical padding XS.
  EdgeInsets get yXS => EdgeInsets.symmetric(vertical: base.xs);

  /// Vertical padding S.
  EdgeInsets get yS => EdgeInsets.symmetric(vertical: base.s);

  /// Vertical padding M.
  EdgeInsets get yM => EdgeInsets.symmetric(vertical: base.m);

  /// Vertical padding L.
  EdgeInsets get yL => EdgeInsets.symmetric(vertical: base.l);

  /// Vertical padding XL.
  EdgeInsets get yXL => EdgeInsets.symmetric(vertical: base.xl);

  /// Vertical padding XXL.
  EdgeInsets get yXXL => EdgeInsets.symmetric(vertical: base.xxl);

  /// Padding XXS.
  EdgeInsets get xxs => EdgeInsets.all(base.xxs);

  /// Padding XS.
  EdgeInsets get xs => EdgeInsets.all(base.xs);

  /// Padding S.
  EdgeInsets get s => EdgeInsets.all(base.s);

  /// Padding M.
  EdgeInsets get m => EdgeInsets.all(base.m);

  /// Padding L.
  EdgeInsets get l => EdgeInsets.all(base.l);

  /// Padding XL.
  EdgeInsets get xl => EdgeInsets.all(base.xl);

  /// Padding XXL.
  EdgeInsets get xxl => EdgeInsets.all(base.xxl);
}
